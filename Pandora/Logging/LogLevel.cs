namespace Pandora.Logging
{
    /// <summary>
    /// Log levels.
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// Information message.
        /// </summary>
        Info = 0,

        /// <summary>
        /// Error message.
        /// </summary>
        Error = 1,

        /// <summary>
        /// Tracing message.
        /// </summary>
        Trace = 2,

        /// <summary>
        /// Debug message.
        /// </summary>
        Debug = 3,
    }
}
